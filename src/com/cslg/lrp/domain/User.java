package src.com.cslg.lrp.domain;

public class User {

    private int userId;
    private String userName;
    private String userSex;
    private int userGrade;
    private String userDept;
    private String userTech;
    private String userSingle;
    private String userNum;
    private String userPassword;

    //以上为用户序号、用户名、用户性别、年级、部门、技术、是否单身、学号（账号）、密码

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserSex() {
        return userSex;
    }

    public void setUserSex(String userSex) {
        this.userSex = userSex;
    }

    public int getUserGrade() {
        return userGrade;
    }

    public void setUserGrade(int userGrade) {
        this.userGrade = userGrade;
    }

    public String getUserDept() {
        return userDept;
    }

    public void setUserDept(String userDept) {
        this.userDept = userDept;
    }

    public String getUserTech() {
        return userTech;
    }

    public void setUserTech(String userTech) {
        this.userTech = userTech;
    }

    public String getUserSingle() {
        return userSingle;
    }

    public void setUserSingle(String userSingle) {
        this.userSingle = userSingle;
    }

    public String getUserNum() {
        return userNum;
    }

    public void setUserNum(String userNum) {
        this.userNum = userNum;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }




}
